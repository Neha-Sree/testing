  static BabySizeInfo getBabySizeForWeek(int week) {
    final data = getBabySizeData();
    
    // Handle invalid weeks gracefully
    if (week < 4) {
      return data[4]!; // Early pregnancy - use week 4 as minimum
    }
    
    if (week > 40) {
      return data[40]!; // Full term - use week 40 as maximum
    }
    
    return data[week] ?? getClosestBabySize(week);
  }

  static BabySizeInfo getClosestBabySize(int week) {
    final data = getBabySizeData();
    
    // Handle invalid weeks
    if (week < 1) {
      return data[4]!; // Default to week 4 for very early pregnancy
    }
    
    if (week > 42) {
      return data[40]!; // Default to week 40 for overdue pregnancies
    }
    
    if (data.containsKey(week)) {
      return data[week]!;
    }
    
    // Find the closest week
    int closestWeek = 4;
    int minDifference = (week - 4).abs();
    
    for (final key in data.keys) {
      final difference = (key - week).abs();
      if (difference < minDifference) {
        minDifference = difference;
        closestWeek = key;
      }
    }
    
    return data[closestWeek]!;
  }

  static String getBabyDescription(int week) {
    final babyInfo = getBabySizeForWeek(week);
    return babyInfo.description;
  }

  static String getBabySizeComparison(int week) {
    final babyInfo = getBabySizeForWeek(week);
    return babyInfo.size;
  }

  static Map<String, String> getBabyMetrics(int week) {
    final babyInfo = getBabySizeForWeek(week);
    return {
      'size': babyInfo.size,
      'length': babyInfo.formattedLength,
      'weight': babyInfo.formattedWeight,
      'description': babyInfo.description,
      'week': babyInfo.week.toString(),
      'developmentQuote': babyInfo.developmentQuote,
      'vegetableAsset': babyInfo.vegetableAsset,
    };
  }
